import (
	"fmt"
	"strings"

	"github.com/1homsi/onekit/internal/onkir"
)

func serverEncodeHelperNames(file *onkir.File) []string {
	seen := map[string]bool{}
	var names []string
	for _, s := range file.Services {
		for _, m := range s.Methods {
			if messageNeedsEncodeHelper(m.Response) && !seen[m.Response.Name] {
				seen[m.Response.Name] = true
				names = append(names, m.Response.Name)
			}
		}
	}
	return names
}

func GenerateServer(file *onkir.File) []byte {
	if len(file.Services) == 0 {
		return nil
	}

	p := &Printer{}
	p.P("// Code generated by onek. DO NOT EDIT.")
	if names := referencedTypeNames(file); len(names) > 0 {
		p.P(`import type { `, strings.Join(names, ", "), ` } from "./types";`)
	}
	if helpers := serverEncodeHelperNames(file); len(helpers) > 0 {
		var fns []string
		for _, h := range helpers {
			fns = append(fns, "encode"+h)
		}
		p.P(`import { `, strings.Join(fns, ", "), ` } from "./types";`)
	}
	p.P()

	writeServerRuntime(p)
	if fileHasStreamMethods(file) {
		writeSSEResponseHelper(p)
	}

	for _, s := range file.Services {
		writeHandlerInterface(p, s)
		writeRouteFactory(p, s)
	}

	return p.Bytes()
}

func writeServerRuntime(p *Printer) {
	p.P("export class HttpError extends Error {")
	p.P("status: number;")
	p.P("body: unknown;")
	p.P("constructor(status: number, body: unknown) {")
	p.P("super(`http error ${status}`);")
	p.P("this.status = status;")
	p.P("this.body = body;")
	p.P("}")
	p.P("}")
	p.P()

	p.P("export interface RouteDescriptor {")
	p.P("method: string;")
	p.P("path: string;")
	p.P("handler: (req: Request) => Promise<Response>;")
	p.P("}")
	p.P()

	p.P("function matchPath(pattern: string, pathname: string): Record<string, string> | null {")
	p.P("const patternParts = pattern.split(\"/\").filter((s) => s.length > 0);")
	p.P("const pathParts = pathname.split(\"/\").filter((s) => s.length > 0);")
	p.P("if (patternParts.length !== pathParts.length) return null;")
	p.P("const params: Record<string, string> = {};")
	p.P("for (let i = 0; i < patternParts.length; i++) {")
	p.P("const part = patternParts[i];")
	p.P("if (part.startsWith(\"{\") && part.endsWith(\"}\")) {")
	p.P("params[part.slice(1, -1)] = decodeURIComponent(pathParts[i]);")
	p.P("} else if (part !== pathParts[i]) {")
	p.P("return null;")
	p.P("}")
	p.P("}")
	p.P("return params;")
	p.P("}")
	p.P()

	p.P("function jsonResponse(body: unknown, status = 200): Response {")
	p.P(`return new Response(JSON.stringify(body), { status, headers: { "Content-Type": "application/json" } });`)
	p.P("}")
	p.P()

	p.P("function errorResponse(err: unknown): Response {")
	p.P("if (err instanceof HttpError) {")
	p.P("return jsonResponse(err.body, err.status);")
	p.P("}")
	p.P(`return jsonResponse({ message: err instanceof Error ? err.message : String(err) }, 500);`)
	p.P("}")
	p.P()
}

func writeHandlerInterface(p *Printer, s *onkir.Service) {
	p.P("export interface ", s.Name, "Handler {")
	for _, m := range s.Methods {
		if m.IsStream() {
			writeSSEHandlerMethod(p, m)
		} else {
			p.P(CamelCase(m.Name), "(req: ", m.Request.Name, "): Promise<", m.Response.Name, ">;")
		}
	}
	p.P("}")
	p.P()
}

func writeRouteFactory(p *Printer, s *onkir.Service) {
	factoryName := "create" + s.Name + "Routes"
	p.P("export function ", factoryName, "(handler: ", s.Name, "Handler): RouteDescriptor[] {")
	p.P("return [")
	for _, m := range s.Methods {
		if m.IsStream() {
			writeSSERoute(p, s, m)
		} else {
			writeRoute(p, s, m)
		}
	}
	p.P("];")
	p.P("}")
	p.P()
}

func writeRoute(p *Printer, s *onkir.Service, m *onkir.Method) {
	verb, _ := m.Verb()
	path, _ := m.Path()
	fullPath := s.BasePath + path
	bodyBearing := isBodyBearingVerb(verb)
	hasPathParams := len(pathParamNames(path)) > 0

	p.P("{")
	p.P(fmt.Sprintf("method: %q,", strings.ToUpper(verb)))
	p.P(fmt.Sprintf("path: %q,", fullPath))
	p.P("handler: async (req: Request): Promise<Response> => {")

	if hasPathParams || !bodyBearing {
		p.P("const url = new URL(req.url);")
	}
	if hasPathParams {
		p.P(fmt.Sprintf("const match = matchPath(%q, url.pathname);", fullPath))
		p.P(`if (!match) return new Response("Not Found", { status: 404 });`)
	}

	p.P("const body: Record<string, unknown> = {};")
	if bodyBearing {
		p.P("Object.assign(body, await req.json());")
	} else {
		writeServerQueryParams(p, m.Request)
	}
	if hasPathParams {
		for _, paramName := range pathParamNames(path) {
			field := findField(m.Request, paramName)
			if field == nil {
				continue
			}
			p.P("body.", field.Name, " = match.", paramName, ";")
		}
	}

	p.P("try {")
	p.P("const result = await handler.", CamelCase(m.Name), "(body as ", m.Request.Name, ");")
	if messageNeedsEncodeHelper(m.Response) {
		p.P("return jsonResponse(encode", m.Response.Name, "(result));")
	} else {
		p.P("return jsonResponse(result);")
	}
	p.P("} catch (err) {")
	p.P("return errorResponse(err);")
	p.P("}")

	p.P("},")
	p.P("},")
}

func writeServerQueryParams(p *Printer, req *onkir.Message) {
	for _, field := range req.Fields {
		d, ok := field.Decorator("query")
		if !ok || field.Type == nil || field.Type.Kind != onkir.KindScalar {
			continue
		}
		queryName, _ := d.Value()
		if queryName == "" {
			queryName = field.Name
		}
		p.P(fmt.Sprintf("const %s = url.searchParams.get(%q);", CamelCase(field.Name), queryName))
		p.P("if (", CamelCase(field.Name), " !== null) {")
		p.P("body.", field.Name, " = ", queryScalarConvert(field.Type.Scalar, CamelCase(field.Name)), ";")
		p.P("}")
	}
}

func queryScalarConvert(kind onkir.ScalarKind, expr string) string {
	switch kind {
	case onkir.ScalarString, onkir.ScalarBytes, onkir.ScalarTimestamp:
		return expr
	case onkir.ScalarBool:
		return expr + " === \"true\""
	case onkir.ScalarInt32, onkir.ScalarInt64, onkir.ScalarUint32, onkir.ScalarUint64:
		return "parseInt(" + expr + ", 10)"
	case onkir.ScalarFloat32, onkir.ScalarFloat64:
		return "parseFloat(" + expr + ")"
	default:
		return expr
	}
}
