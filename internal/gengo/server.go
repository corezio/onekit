package gengo

import (
	"fmt"
	"regexp"
	"strings"

	"github.com/1homsi/onekit/internal/onkir"
)

var pathParamPattern = regexp.MustCompile(`\{(\w+)\}`)

func pathParamNames(path string) []string {
	var names []string
	for _, m := range pathParamPattern.FindAllStringSubmatch(path, -1) {
		names = append(names, m[1])
	}
	return names
}

func findField(msg *onkir.Message, name string) *onkir.Field {
	for _, f := range msg.Fields {
		if f.Name == name {
			return f
		}
	}
	return nil
}

func scalarParseExpr(kind onkir.ScalarKind, strExpr string) (string, bool) {
	switch kind {
	case onkir.ScalarString:
		return strExpr, true
	case onkir.ScalarInt32:
		return fmt.Sprintf("parseInt32(%s)", strExpr), true
	case onkir.ScalarInt64:
		return fmt.Sprintf("parseInt64(%s)", strExpr), true
	case onkir.ScalarUint32:
		return fmt.Sprintf("parseUint32(%s)", strExpr), true
	case onkir.ScalarUint64:
		return fmt.Sprintf("parseUint64(%s)", strExpr), true
	case onkir.ScalarFloat32:
		return fmt.Sprintf("parseFloat32(%s)", strExpr), true
	case onkir.ScalarFloat64:
		return fmt.Sprintf("parseFloat64(%s)", strExpr), true
	case onkir.ScalarBool:
		return fmt.Sprintf("parseBool(%s)", strExpr), true
	default:
		return "", false
	}
}

func GenerateServer(file *onkir.File) ([]byte, error) {
	if len(file.Services) == 0 {
		return nil, nil
	}

	p := &Printer{}
	p.P("// Code generated by onek. DO NOT EDIT.")
	p.P("package ", GoPackageName(file))
	p.P()
	p.P("import (")
	p.P(`"context"`)
	p.P(`"encoding/json"`)
	p.P(`"net/http"`)
	p.P(`"strconv"`)
	p.P(")")
	p.P()

	writeRuntimeHelpers(p)

	for _, s := range file.Services {
		writeServiceInterface(p, s)
		writeRegisterFunc(p, s)
	}

	return p.Format()
}

func writeRuntimeHelpers(p *Printer) {
	p.P(`func writeJSONError(w http.ResponseWriter, status int, message string) {`)
	p.P(`w.Header().Set("Content-Type", "application/json")`)
	p.P(`w.WriteHeader(status)`)
	p.P(`_ = json.NewEncoder(w).Encode(map[string]string{"message": message})`)
	p.P(`}`)
	p.P()
	p.P(`func parseInt32(s string) int32 { v, _ := strconv.ParseInt(s, 10, 32); return int32(v) }`)
	p.P(`func parseInt64(s string) int64 { v, _ := strconv.ParseInt(s, 10, 64); return v }`)
	p.P(`func parseUint32(s string) uint32 { v, _ := strconv.ParseUint(s, 10, 32); return uint32(v) }`)
	p.P(`func parseUint64(s string) uint64 { v, _ := strconv.ParseUint(s, 10, 64); return v }`)
	p.P(`func parseFloat32(s string) float32 { v, _ := strconv.ParseFloat(s, 32); return float32(v) }`)
	p.P(`func parseFloat64(s string) float64 { v, _ := strconv.ParseFloat(s, 64); return v }`)
	p.P(`func parseBool(s string) bool { v, _ := strconv.ParseBool(s); return v }`)
	p.P()
}

func writeServiceInterface(p *Printer, s *onkir.Service) {
	p.P("type ", s.Name, "Server interface {")
	for _, m := range s.Methods {
		p.P(PascalCase(m.Name), "(ctx context.Context, req *", m.Request.Name, ") (*", m.Response.Name, ", error)")
	}
	p.P("}")
	p.P()
}

func writeRegisterFunc(p *Printer, s *onkir.Service) {
	p.P("func Register", s.Name, "Server(mux *http.ServeMux, srv ", s.Name, "Server) {")
	for _, m := range s.Methods {
		writeRoute(p, s, m)
	}
	p.P("}")
	p.P()
}

func writeRoute(p *Printer, s *onkir.Service, m *onkir.Method) {
	verb, _ := m.Verb()
	path, _ := m.Path()
	fullPath := s.BasePath + path
	bodyBearing := verb == "post" || verb == "put" || verb == "patch" || verb == "query"

	p.P(`mux.HandleFunc("`, strings.ToUpper(verb), " ", fullPath, `", func(w http.ResponseWriter, r *http.Request) {`)
	p.P("req := new(", m.Request.Name, ")")

	if bodyBearing {
		p.P("if r.Body != nil {")
		p.P("if err := json.NewDecoder(r.Body).Decode(req); err != nil && err.Error() != \"EOF\" {")
		p.P(`writeJSONError(w, http.StatusBadRequest, "invalid request body: "+err.Error())`)
		p.P("return")
		p.P("}")
		p.P("}")
	}

	for _, paramName := range pathParamNames(path) {
		field := findField(m.Request, paramName)
		if field == nil || field.Type == nil || field.Type.Kind != onkir.KindScalar {
			continue
		}
		expr, ok := scalarParseExpr(field.Type.Scalar, fmt.Sprintf("r.PathValue(%q)", paramName))
		if !ok {
			continue
		}
		p.P("req.", PascalCase(paramName), " = ", expr)
	}

	if !bodyBearing {
		for _, field := range m.Request.Fields {
			d, ok := field.Decorator("query")
			if !ok || field.Type == nil || field.Type.Kind != onkir.KindScalar {
				continue
			}
			queryName, _ := d.Value()
			if queryName == "" {
				queryName = field.Name
			}
			strExpr := fmt.Sprintf("r.URL.Query().Get(%q)", queryName)
			expr, ok := scalarParseExpr(field.Type.Scalar, strExpr)
			if !ok {
				continue
			}
			p.P("if ", strExpr, " != \"\" {")
			p.P("req.", PascalCase(field.Name), " = ", expr)
			p.P("}")
		}
	}

	for _, h := range m.Service.Headers {
		writeHeaderCheck(p, h)
	}
	for _, h := range m.Headers {
		writeHeaderCheck(p, h)
	}

	p.P("if v, ok := any(req).(interface{ Validate() error }); ok {")
	p.P("if err := v.Validate(); err != nil {")
	p.P(`writeJSONError(w, http.StatusBadRequest, err.Error())`)
	p.P("return")
	p.P("}")
	p.P("}")

	p.P("resp, err := srv.", PascalCase(m.Name), "(r.Context(), req)")
	p.P("if err != nil {")
	writeErrorHandling(p, m)
	p.P("return")
	p.P("}")
	p.P(`w.Header().Set("Content-Type", "application/json")`)
	p.P("_ = json.NewEncoder(w).Encode(resp)")
	p.P("})")
}

func writeHeaderCheck(p *Printer, h *onkir.Header) {
	if !h.Required() {
		return
	}
	p.P(`if r.Header.Get("`, h.Name, `") == "" {`)
	p.P(`writeJSONError(w, http.StatusBadRequest, "missing required header: `, h.Name, `")`)
	p.P("return")
	p.P("}")
}

func writeErrorHandling(p *Printer, m *onkir.Method) {
	if len(m.ErrorTypes) == 0 {
		p.P(`writeJSONError(w, http.StatusInternalServerError, err.Error())`)
		return
	}
	p.P("switch e := err.(type) {")
	for _, errType := range m.ErrorTypes {
		status := 500
		if code, ok := errType.StatusCode(); ok {
			status = code
		}
		p.P("case *", errType.Name, ":")
		p.P(`w.Header().Set("Content-Type", "application/json")`)
		p.P(fmt.Sprintf("w.WriteHeader(%d)", status))
		p.P("_ = json.NewEncoder(w).Encode(e)")
	}
	p.P("default:")
	p.P(`writeJSONError(w, http.StatusInternalServerError, err.Error())`)
	p.P("}")
}
