package gengo

import (
	"fmt"
	"strings"

	"github.com/1homsi/onekit/internal/onkir"
)

func GoPackageName(file *onkir.File) string {
	parts := strings.Split(file.Package, ".")
	if len(parts) == 0 {
		return "generated"
	}
	return strings.ToLower(parts[len(parts)-1])
}

func needsTimeImport(file *onkir.File) bool {
	for _, m := range file.Messages {
		if messageUsesTime(m) {
			return true
		}
	}
	return false
}

func messageUsesTime(m *onkir.Message) bool {
	for _, f := range m.Fields {
		if f.Type != nil && typeUsesTime(f.Type) {
			return true
		}
	}
	for _, nested := range m.Nested {
		if messageUsesTime(nested) {
			return true
		}
	}
	return false
}

func typeUsesTime(t *onkir.Type) bool {
	switch t.Kind {
	case onkir.KindScalar:
		return t.Scalar == onkir.ScalarTimestamp
	case onkir.KindMap:
		return typeUsesTime(t.MapValue)
	case onkir.KindMessage, onkir.KindEnum:
		return false
	default:
		return false
	}
}

func hasOneofMessages(file *onkir.File) bool {
	for _, m := range file.Messages {
		if messageOrNestedHasOneof(m) {
			return true
		}
	}
	return false
}

func messageOrNestedHasOneof(m *onkir.Message) bool {
	if hasOneofField(m) {
		return true
	}
	for _, nested := range m.Nested {
		if messageOrNestedHasOneof(nested) {
			return true
		}
	}
	return false
}

func hasOneofField(m *onkir.Message) bool {
	return len(oneofFields(m)) > 0
}

func oneofFields(m *onkir.Message) []*onkir.Field {
	var fields []*onkir.Field
	for _, f := range m.Fields {
		if f.Oneof != nil {
			fields = append(fields, f)
		}
	}
	return fields
}

func hasErrorMessages(file *onkir.File) bool {
	for _, m := range file.Messages {
		if messageOrNestedHasError(m) {
			return true
		}
	}
	return false
}

func messageOrNestedHasError(m *onkir.Message) bool {
	if m.IsError() {
		return true
	}
	for _, nested := range m.Nested {
		if messageOrNestedHasError(nested) {
			return true
		}
	}
	return false
}

func GenerateTypes(file *onkir.File) ([]byte, error) {
	p := &Printer{}
	p.P("// Code generated by onek. DO NOT EDIT.")
	p.P("package ", GoPackageName(file))
	p.P()

	needsTime := needsTimeImport(file)
	needsErrorHelpers := hasErrorMessages(file)
	needsJSON := hasOneofMessages(file)
	if needsTime || needsErrorHelpers || needsJSON {
		p.P("import (")
		if needsJSON {
			p.P(`"encoding/json"`)
		}
		if needsErrorHelpers {
			p.P(`"fmt"`)
		}
		if needsErrorHelpers {
			p.P(`"strings"`)
		}
		if needsTime {
			p.P(`"time"`)
		}
		p.P(")")
		p.P()
	}

	for _, e := range file.Enums {
		writeEnum(p, e)
	}
	for _, m := range file.Messages {
		writeMessage(p, m)
	}

	return p.Format()
}

func writeEnum(p *Printer, e *onkir.Enum) {
	p.P("type ", e.Name, " int32")
	p.P()
	p.P("const (")
	for i, v := range e.Values {
		constName := e.Name + PascalCase(strings.ToLower(v.Name))
		if i == 0 {
			p.P(constName, " ", e.Name, " = iota")
		} else {
			p.P(constName)
		}
	}
	p.P(")")
	p.P()

	p.P("func (v ", e.Name, ") String() string {")
	p.P("switch v {")
	for _, v := range e.Values {
		constName := e.Name + PascalCase(strings.ToLower(v.Name))
		p.P("case ", constName, ":")
		p.P("return ", fmt.Sprintf("%q", v.JSONName()))
	}
	p.P("default:")
	p.P(`return "unknown"`)
	p.P("}")
	p.P("}")
	p.P()
}

func writeMessage(p *Printer, m *onkir.Message) {
	p.P("type ", m.Name, " struct {")
	for _, f := range m.Fields {
		writeField(p, m, f)
	}
	p.P("}")
	p.P()

	if m.IsError() {
		writeErrorMethod(p, m)
	}
	if hasOneofField(m) {
		writeOneofJSONMethods(p, m)
	}

	for _, f := range m.Fields {
		if f.Oneof != nil {
			writeOneof(p, m, f)
		}
	}
	for _, nested := range m.Nested {
		writeMessage(p, nested)
	}
	for _, nested := range m.NestedEnums {
		writeEnum(p, nested)
	}
}

func oneofDiscriminatorName(f *onkir.Field) string {
	if disc, ok := f.Oneof.Discriminator(); ok && disc != "" {
		return disc
	}
	return "type"
}

// writeOneofJSONMethods generates MarshalJSON/UnmarshalJSON for messages
// with oneof fields, since a Go interface-typed field can't be (de)serialized
// by encoding/json without help: marshaling needs to pick the discriminator
// tag for whichever concrete variant is set, and unmarshaling needs to read
// the discriminator back out of the raw JSON before it can construct the
// right concrete type. The `type alias X` trick shadows the oneof field at
// a shallower struct depth so the promoted field from the embedded alias
// never fights the raw-JSON one for the same key.
func writeOneofJSONMethods(p *Printer, m *onkir.Message) {
	writeOneofMarshalJSON(p, m)
	writeOneofUnmarshalJSON(p, m)
}

func writeOneofMarshalJSON(p *Printer, m *onkir.Message) {
	oneofs := oneofFields(m)

	p.P("func (m *", m.Name, ") MarshalJSON() ([]byte, error) {")
	p.P("type alias ", m.Name)
	p.P("aux := struct {")
	p.P("*alias")
	for _, f := range oneofs {
		p.P(PascalCase(f.Name), " json.RawMessage `json:\"", f.Name, ",omitempty\"`")
	}
	p.P("}{alias: (*alias)(m)}")
	for _, f := range oneofs {
		goName := PascalCase(f.Name)
		discriminator := oneofDiscriminatorName(f)
		p.P("if m.", goName, " != nil {")
		p.P("var obj map[string]any")
		p.P("switch v := m.", goName, ".(type) {")
		for _, variant := range f.Oneof.Variants {
			typeName := OneofVariantTypeName(m, f, variant)
			p.P("case *", typeName, ":")
			p.P(fmt.Sprintf(
				"obj = map[string]any{%q: %q, %q: v.%s}",
				discriminator, variant.Tag(), variant.Name, PascalCase(variant.Name),
			))
		}
		p.P("}")
		p.P("objBytes, err := json.Marshal(obj)")
		p.P("if err != nil {")
		p.P("return nil, err")
		p.P("}")
		p.P("aux.", goName, " = objBytes")
		p.P("}")
	}
	p.P("return json.Marshal(aux)")
	p.P("}")
	p.P()
}

func writeOneofUnmarshalJSON(p *Printer, m *onkir.Message) {
	oneofs := oneofFields(m)

	p.P("func (m *", m.Name, ") UnmarshalJSON(data []byte) error {")
	p.P("type alias ", m.Name)
	p.P("aux := struct {")
	p.P("*alias")
	for _, f := range oneofs {
		p.P(PascalCase(f.Name), " json.RawMessage `json:\"", f.Name, ",omitempty\"`")
	}
	p.P("}{alias: (*alias)(m)}")
	p.P("if err := json.Unmarshal(data, &aux); err != nil {")
	p.P("return err")
	p.P("}")
	for _, f := range oneofs {
		goName := PascalCase(f.Name)
		discriminator := oneofDiscriminatorName(f)
		p.P("if len(aux.", goName, ") > 0 {")
		p.P("var disc struct {")
		p.P("Tag string `json:\"", discriminator, "\"`")
		p.P("}")
		p.P("if err := json.Unmarshal(aux.", goName, ", &disc); err != nil {")
		p.P("return err")
		p.P("}")
		p.P("switch disc.Tag {")
		for _, variant := range f.Oneof.Variants {
			typeName := OneofVariantTypeName(m, f, variant)
			innerGoType := GoFieldType(variant.Type)
			p.P("case ", fmt.Sprintf("%q", variant.Tag()), ":")
			p.P("var v struct {")
			p.P("Val ", innerGoType, " `json:\"", variant.Name, "\"`")
			p.P("}")
			p.P("if err := json.Unmarshal(aux.", goName, ", &v); err != nil {")
			p.P("return err")
			p.P("}")
			p.P("m.", goName, " = &", typeName, "{", PascalCase(variant.Name), ": v.Val}")
		}
		p.P("}")
		p.P("}")
	}
	p.P("return nil")
	p.P("}")
	p.P()
}

func writeErrorMethod(p *Printer, m *onkir.Message) {
	p.P("func (m *", m.Name, ") Error() string {")
	p.P(`parts := []string{"`, m.Name, `"}`)
	for _, f := range m.Fields {
		if f.Oneof != nil {
			continue
		}
		p.P(`parts = append(parts, fmt.Sprintf("`, f.Name, `=%v", m.`, PascalCase(f.Name), "))")
	}
	p.P(`return strings.Join(parts, " ")`)
	p.P("}")
	p.P()
}

func writeField(p *Printer, m *onkir.Message, f *onkir.Field) {
	goName := PascalCase(f.Name)
	if f.Oneof != nil {
		p.P(goName, " ", OneofInterfaceName(m, f), " `json:\"", f.Name, ",omitempty\"`")
		return
	}

	goType := GoFieldType(f.Type)
	if f.Repeated {
		goType = "[]" + goType
	} else if f.Optional && f.Type.Kind != onkir.KindMessage {
		goType = "*" + goType
	}

	p.P(goName, " ", goType, " `json:\"", f.Name, ",omitempty\"`")
}

func writeOneof(p *Printer, m *onkir.Message, f *onkir.Field) {
	iface := OneofInterfaceName(m, f)
	marker := "is" + iface

	p.P("type ", iface, " interface {")
	p.P(marker, "()")
	p.P("}")
	p.P()

	for _, variant := range f.Oneof.Variants {
		typeName := OneofVariantTypeName(m, f, variant)
		p.P("type ", typeName, " struct {")
		p.P(PascalCase(variant.Name), " ", GoFieldType(variant.Type), " `json:\"", variant.Name, ",omitempty\"`")
		p.P("}")
		p.P()
		p.P("func (*", typeName, ") ", marker, "() {}")
		p.P()
	}
}
